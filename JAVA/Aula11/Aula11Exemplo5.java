public class Aula11Exemplo5 {
    public static void main(String[] args) {
        int M[][] = new int[2][2];
        M[0][0] = 1;
        M[0][1] = 4;
        M[1][0] = 7;
        M[1][1] = 9;

        System.out.println(M[0][0] + "\t" + M[0][1]);
        System.out.println(M[1][0] + "\t" + M[1][1]);
    }
}